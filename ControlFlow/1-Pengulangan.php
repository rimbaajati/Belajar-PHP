<?php 
//Perulangan For
for ($i = 0; $i < 10; $i++) {
    echo"Perulangan $i <br>";
}

//Perulangan While
$a=1;
while ($a < 10) {   //Selama kondisi true, lakukan apa yg didalamnya!
    echo "Hellow Maria <br>";
    $a++;
}

//Perulangan Do While
//Ketika Kondisi false maka akan dijalankan 1x
$b=11;
do {
    echo "Hola Bonjour";
    $b++;
}while ($b < 5) ;

